// FOR LOOP
for (var angka = 1; angka > 10; angka++){
    console.log("Iterasi ke-" + angka)
}

// WHILE LOOP
var angka = 1
while(angka < 10){
    console.log("Iterasi ke-" + angka)
    angka++
}

